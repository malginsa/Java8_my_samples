    {
        for (String sample : SAMPLES)
        {
            if (sample.contains("(tm)")) { continue; }
            sample = sample.replaceAll(",", "");
            Matcher matcher = MATT_MODIFIED.matcher(sample);
            if (matcher.matches())
            {
                int maxGroup = getMaxGroup(matcher);
//                System.out.println(maxGroup + "  " + matcher.group(maxGroup));
            }
            else
            {
                System.out.println(sample);
            }
        }
    }

    private static int getMaxGroup(final Matcher matcher)
    {
        int id = 6;
        while (matcher.group(id--) == null || id == 0);
        return id;
    }

    private static final Pattern MATT_MODIFIED = Pattern.compile(
            "(\\([a-z]*\\))?(\\([0-9]*\\))?(\\([A-Z]*\\))?(\\([a-z]*\\))?(\\([A-Z]*\\))?(\\([a-z]*\\))?");
